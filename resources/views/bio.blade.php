  <h5>2nd Album(EP) - Reaching for the Skies</h5>
  <p>Ribbons of Euphoria  return with their second release, promisingly entitled Reaching for the Skies. The rocking quartet from Athens-Greece, combine the 60’s psychedelic sound with the 70’s progressive aesthetics and approach. Pure rock, in large doses, satisfying even to the most demanding of the genre. Influences from Cream to Iron Butterfly and from Mountain to Camel and Uriah Heep are evident; however the approach is original and fresh. The EP includes two tracks of 17 minutes total play time. Rich compositions include impressive polyphonous orchestrations, organic and meaningful guitar riffs and beautiful orchestral parts, cleverly structured, where the organ and the guitar switch leading roles. Each hearing reveals little, hidden compositional treasures, as the music of the Ribbons best unveils itself to the faithful.</p>
